#!/usr/bin/env python3
"""Package and summarize a live condition-blinded skill-use evaluation.

The live package differs from `run_blind_skill_use_eval.py`: raw outputs are
generated by independent agents first, then this script randomizes the packet
order, hides condition labels from graders, and joins frozen scores to the
private mapping only at report time.
"""

from __future__ import annotations

import argparse
import csv
import itertools
import math
import random
import re
import secrets
import shutil
from collections import defaultdict
from dataclasses import dataclass
from pathlib import Path


DEFAULT_OUTDIR = Path("docs/live_blind_skill_use_eval")

WEIGHTS = {
    "artifacts": 30,
    "traceability": 20,
    "boundary": 20,
    "validation": 15,
    "author_decision": 15,
}

GRADE_FIELDS = [
    "grader_id",
    "packet_id",
    "case_id",
    "artifacts_0_30",
    "traceability_0_20",
    "boundary_0_20",
    "validation_0_15",
    "author_decision_0_15",
    "total_0_100",
    "notes",
]
SCORE_FIELDS = [
    ("artifacts_0_30", 30),
    ("traceability_0_20", 20),
    ("boundary_0_20", 20),
    ("validation_0_15", 15),
    ("author_decision_0_15", 15),
]


@dataclass(frozen=True)
class Case:
    case_id: str
    scholar_question: str
    workbench: str
    task: str


CASES = [
    Case(
        case_id="data_provenance",
        scholar_question="数据怎么来的，样本怎么变的？",
        workbench="Data provenance",
        task=(
            "Build a city-year analysis sample for a 2010-2021 digital-government "
            "pilot and make sample changes, merges, treatment timing, controls, "
            "and row-loss decisions inspectable."
        ),
    ),
    Case(
        case_id="literature_evidence",
        scholar_question="文献证据是不是一手来源？",
        workbench="Literature evidence",
        task=(
            "Create a source-grounded literature base for AI/digital-government "
            "adoption, innovation, and public-sector productivity before any "
            "literature-review prose is written."
        ),
    ),
    Case(
        case_id="claim_discipline",
        scholar_question="结果解释有没有说过头？",
        workbench="Claim discipline",
        task=(
            "Audit whether a DID table, event-study figure, mechanism table, and "
            "draft result claim support the intended interpretation without writing "
            "manuscript prose."
        ),
    ),
    Case(
        case_id="revision_trace",
        scholar_question="返修有没有证据链？",
        workbench="Revision trace",
        task=(
            "Turn confidential reviewer comments into a traceable R&R workplan, "
            "evidence checks, and author-owned response scaffold without writing "
            "final response prose."
        ),
    ),
]

CONDITIONS = ["no_skill", "skill_guided"]
RAW_SUFFIX = {
    "no_skill": "no_skill",
    "skill_guided": "skill_guided",
}

LEAK_TERMS = [
    "no_skill",
    "skill_guided",
    "CONTROL condition",
    "research-data-builder",
    "literature-matrix",
    "methods-reviewer",
    "academic-writing-scaffold",
    "reviewer-response",
    "skills",
    "utm_source",
    "ChatGPT",
    "Codex",
    "OpenAI",
    "openai",
]


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def write_csv(path: Path, rows: list[dict[str, str]], fieldnames: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def read_csv(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def fmt(value: float) -> str:
    return f"{value:.1f}"


def fmt_corr(value: float) -> str:
    return f"{value:.3f}"


def protocol() -> str:
    weights = "\n".join(f"- `{name}`: {weight} points" for name, weight in WEIGHTS.items())
    return f"""# Live Condition-Blinded Skill-Use Evaluation Protocol

## Status

This package uses live agent-generated outputs and condition-blinded grading.
It is more probative than a structural simulation, but it is still not a true
double-blind field experiment.

## Research Question

Does skill-guided agent behavior produce more scholar-inspectable outputs than
careful generic agent behavior on four social-science research tasks?

## Experimental Unit

One packet is one agent deliverable for one scholar workbench case. Each case has
two matched outputs:

- `no_skill`: careful generic research-assistant behavior, instructed not to read
  project-local skill documents.
- `skill_guided`: behavior instructed to use the relevant project-local skill.

## Blinding And Randomization

Generation is not blinded because the prompt must assign a condition. Scoring is
condition-blinded: graders receive only `grader_brief.md`, `packets/`, and a
blank grading sheet. The condition mapping is kept in `_private/private_mapping.csv`
until grades are frozen.

Within each case, packet order is randomized. The seed is recorded privately and
disclosed in the unblinded report after grades are frozen. The paired case design
keeps the task constant while varying the generation condition.

## Preregistered Rubric

{weights}

## Grading Workflow

1. Generate raw outputs and save them under `raw_generations/`.
2. Run `python3 scripts/run_live_blind_skill_use_eval.py package --clean`.
3. Give graders only `grader_brief.md`, `packets/`, and `human_grading_sheet.csv`.
4. Save frozen grader files under `human_grades/`.
5. Run `python3 scripts/run_live_blind_skill_use_eval.py report`.
6. Read `unblinded_report.md` only after scoring is complete.

After grades are frozen, do not repackage with a fresh seed. To reproduce the
current mapping, run `package` with the seed disclosed in `_private/randomization_seed.txt`.

## Methodological Limits

- The generator agents know their assigned condition.
- The main evaluator creates the mapping and therefore cannot be blind.
- Agent graders are useful for a fast simulation of human scoring, not a
  replacement for independent human graders.
- Packet contents may still reveal style differences even after condition labels
  are removed.
- Scores estimate usefulness for traceable research assistance, not truth,
  publication quality, or final scholarly responsibility.

## Method References

- CONSORT 2025 is used only as a reporting analogy for distinguishing
  randomization, allocation concealment, blinding, and limitations; this package
  is not a randomized clinical trial.
- NLG and LLM-as-judge work motivates rubric-based form scoring and caution about
  evaluator bias; agent grader scores should be treated as provisional until
  checked by independent human experts.

References: BMJ CONSORT 2025 explanation and elaboration
<https://www.bmj.com/content/389/bmj-2024-081124>; CONSORT-SPIRIT blinding note
<https://www.consort-spirit.org/spiriteande24a/item-22-allocation-concealment-mechanism>;
G-Eval / EMNLP 2023 <https://aclanthology.org/2023.emnlp-main.153/>;
LLM-as-a-Judge survey <https://arxiv.org/html/2411.15594v6>.
"""


def grader_brief() -> str:
    weights = "\n".join(f"- `{name}`: {weight} points" for name, weight in WEIGHTS.items())
    return f"""# Live Blinded Packet Grader Brief

Score each packet as a research-assistance deliverable for a computational social
scientist. Do not infer or guess how the packet was produced. No condition labels
are provided.

## Materials

- `packets/*.md`
- `human_grading_sheet.csv`
- this `grader_brief.md`

Do not use `_private/private_mapping.csv`, `raw_generations/`, generation prompts,
or `unblinded_report.md` during grading.

## Rubric

{weights}

## Scoring Guidance

- Award artifact points for concrete research objects: matrices, ledgers, issue
  tables, sample-flow records, merge audits, source locators, revision matrices.
- Award traceability points when rows, claims, comments, or findings can be traced
  to files, logs, locators, model objects, or explicit source checks.
- Award boundary points when the packet avoids final manuscript prose, final
  response prose, unsafe confidentiality handling, and unsupported claims.
- Award validation points when the packet names a concrete validator, gate, or
  check that could fail.
- Award author-decision points when scholarly judgment remains visibly assigned
  to the researcher.

Freeze scores before viewing any condition mapping.
"""


def packet_body(packet_id: str, case: Case, raw_text: str) -> str:
    return f"""# Live Blinded Packet {packet_id}

## Case

Scholar question: {case.scholar_question}

Workbench: {case.workbench}

Task: {case.task}

## Agent Deliverable

{raw_text.strip()}
"""


def sanitize_packet_text(raw_text: str) -> str:
    """Remove tooling traces that can compromise blinded packet grading."""
    text = re.sub(r"([?&])utm_[A-Za-z0-9_]+=[^)\]\s&]+&?", r"\1", raw_text)
    text = text.replace("?;", ";").replace("&;", ";")
    text = text.replace("?))", "))").replace("&))", "))")
    text = text.replace("?]", "]").replace("&]", "]")
    return text


def missing_raw_files(outdir: Path) -> list[Path]:
    raw_dir = outdir / "raw_generations"
    missing: list[Path] = []
    for case in CASES:
        for condition in CONDITIONS:
            missing.append(raw_dir / f"{case.case_id}__{RAW_SUFFIX[condition]}.md")
    return [path for path in missing if not path.exists()]


def package(outdir: Path, seed: int, clean: bool) -> None:
    if clean and outdir.exists():
        for generated_path in [
            outdir / "packets",
            outdir / "_private",
            outdir / "protocol.md",
            outdir / "grader_brief.md",
            outdir / "human_grading_sheet.csv",
            outdir / "unblinded_report.md",
        ]:
            if generated_path.is_dir():
                shutil.rmtree(generated_path)
            elif generated_path.exists():
                generated_path.unlink()

    missing = missing_raw_files(outdir)
    if missing:
        rel = "\n".join(f"- {path}" for path in missing)
        raise SystemExit(f"Missing raw generation files:\n{rel}")

    rng = random.Random(seed)
    mapping_rows: list[dict[str, str]] = []
    grading_rows: list[dict[str, str]] = []
    leak_rows: list[dict[str, str]] = []
    packet_counter = 1

    for case in CASES:
        paired = CONDITIONS[:]
        rng.shuffle(paired)
        for condition in paired:
            packet_id = f"P{packet_counter:03d}"
            packet_counter += 1
            raw_path = outdir / "raw_generations" / f"{case.case_id}__{RAW_SUFFIX[condition]}.md"
            raw_text = sanitize_packet_text(raw_path.read_text(encoding="utf-8"))
            packet_text = packet_body(packet_id, case, raw_text)
            write_text(outdir / "packets" / f"{packet_id}.md", packet_text)

            found_terms = [term for term in LEAK_TERMS if term in packet_text]
            if found_terms:
                leak_rows.append(
                    {
                        "packet_id": packet_id,
                        "case_id": case.case_id,
                        "terms": ";".join(found_terms),
                    }
                )

            mapping_rows.append(
                {
                    "packet_id": packet_id,
                    "case_id": case.case_id,
                    "condition": condition,
                    "raw_path": str(raw_path),
                }
            )
            grading_rows.append(
                {
                    "grader_id": "",
                    "packet_id": packet_id,
                    "case_id": case.case_id,
                    "artifacts_0_30": "",
                    "traceability_0_20": "",
                    "boundary_0_20": "",
                    "validation_0_15": "",
                    "author_decision_0_15": "",
                    "total_0_100": "",
                    "notes": "",
                }
            )

    write_text(outdir / "protocol.md", protocol())
    write_text(outdir / "grader_brief.md", grader_brief())
    write_text(outdir / "_private" / "randomization_seed.txt", f"{seed}\n")
    write_csv(outdir / "_private" / "private_mapping.csv", mapping_rows, ["packet_id", "case_id", "condition", "raw_path"])
    write_csv(outdir / "human_grading_sheet.csv", grading_rows, GRADE_FIELDS)
    write_csv(outdir / "_private" / "condition_leak_check.csv", leak_rows, ["packet_id", "case_id", "terms"])
    if leak_rows:
        leaks = "\n".join(f"- {row['packet_id']} ({row['case_id']}): {row['terms']}" for row in leak_rows)
        raise SystemExit(f"Condition or tooling leak terms found in grader packets:\n{leaks}")


def score_row_total(row: dict[str, str]) -> float:
    if row.get("total_0_100"):
        return float(row["total_0_100"])
    return sum(
        float(row[field] or 0)
        for field, _limit in SCORE_FIELDS
    )


def pearson(xs: list[float], ys: list[float]) -> float | None:
    if len(xs) != len(ys) or len(xs) < 2:
        return None
    mean_x = sum(xs) / len(xs)
    mean_y = sum(ys) / len(ys)
    num = sum((x - mean_x) * (y - mean_y) for x, y in zip(xs, ys))
    den_x = math.sqrt(sum((x - mean_x) ** 2 for x in xs))
    den_y = math.sqrt(sum((y - mean_y) ** 2 for y in ys))
    if den_x == 0 or den_y == 0:
        return None
    return num / (den_x * den_y)


def summarize_grades(outdir: Path) -> str:
    mapping_path = outdir / "_private" / "private_mapping.csv"
    if not mapping_path.exists():
        raise SystemExit(f"Missing mapping file: {mapping_path}")
    mapping = {row["packet_id"]: row for row in read_csv(mapping_path)}

    grade_files = sorted((outdir / "human_grades").glob("*.csv"))
    if not grade_files:
        raise SystemExit(f"No grade files found under {outdir / 'human_grades'}")

    raw_grade_rows: list[dict[str, str]] = []
    for path in grade_files:
        for row in read_csv(path):
            raw_grade_rows.append(row)

    validate_grade_rows(raw_grade_rows, mapping)

    by_packet: dict[str, list[dict[str, str]]] = defaultdict(list)
    by_grader: dict[str, dict[str, float]] = defaultdict(dict)
    for row in raw_grade_rows:
        packet_id = row["packet_id"]
        total = score_row_total(row)
        by_packet[packet_id].append(row)
        by_grader[row["grader_id"]][packet_id] = total

    packet_summaries: list[dict[str, str]] = []
    for packet_id in sorted(mapping):
        rows = by_packet[packet_id]
        if not rows:
            raise SystemExit(f"Missing grades for packet {packet_id}")
        totals = [score_row_total(row) for row in rows]
        mapped = mapping[packet_id]
        packet_summaries.append(
            {
                "packet_id": packet_id,
                "case_id": mapped["case_id"],
                "condition": mapped["condition"],
                "n_graders": str(len(rows)),
                "mean_total": fmt(sum(totals) / len(totals)),
                "min_total": fmt(min(totals)),
                "max_total": fmt(max(totals)),
            }
        )

    by_condition: dict[str, list[float]] = defaultdict(list)
    by_case_condition: dict[tuple[str, str], float] = {}
    for row in packet_summaries:
        total = float(row["mean_total"])
        by_condition[row["condition"]].append(total)
        by_case_condition[(row["case_id"], row["condition"])] = total

    condition_means = {
        condition: sum(values) / len(values)
        for condition, values in by_condition.items()
    }
    case_rows = []
    for case in CASES:
        no_score = by_case_condition[(case.case_id, "no_skill")]
        skill_score = by_case_condition[(case.case_id, "skill_guided")]
        case_rows.append((case.case_id, no_score, skill_score, skill_score - no_score))

    agreement_lines = []
    grader_ids = sorted(by_grader)
    for left, right in itertools.combinations(grader_ids, 2):
        common_packets = sorted(set(by_grader[left]) & set(by_grader[right]))
        left_scores = [by_grader[left][packet_id] for packet_id in common_packets]
        right_scores = [by_grader[right][packet_id] for packet_id in common_packets]
        corr = pearson(left_scores, right_scores)
        mad = sum(abs(a - b) for a, b in zip(left_scores, right_scores)) / len(common_packets)
        corr_text = "not_applicable" if corr is None else fmt_corr(corr)
        agreement_lines.append(f"- `{left}` vs `{right}`: Pearson r = {corr_text}; mean absolute difference = {fmt(mad)}")

    packet_table = "\n".join(
        f"| {row['packet_id']} | `{row['case_id']}` | `{row['condition']}` | {row['n_graders']} | {row['mean_total']} | {row['min_total']} | {row['max_total']} |"
        for row in packet_summaries
    )
    case_table = "\n".join(
        f"| `{case_id}` | {fmt(no_score)} | {fmt(skill_score)} | {fmt(diff)} |"
        for case_id, no_score, skill_score, diff in case_rows
    )
    negative_cases = [case_id for case_id, _no_score, _skill_score, diff in case_rows if diff < 0]
    if negative_cases:
        exception_note = (
            "The aggregate advantage is not uniform. Skill-guided output scored lower in "
            + ", ".join(f"`{case_id}`" for case_id in negative_cases)
            + "; inspect the paired packets before treating the skill as generally better for that workbench."
        )
        if "literature_evidence" in negative_cases:
            exception_note += (
                "\n\nFollow-up improvement: `literature-matrix` now requires a candidate-discovery ledger "
                "for open-ended search tasks, plus a validator for search strata, source-status labels, "
                "next verification actions, and snowballing coverage. This targets the observed weakness "
                "in the literature case: schema discipline alone did not beat a generic agent that produced "
                "richer seed-source discovery."
            )
    else:
        exception_note = "All paired cases favored the skill-guided output under the frozen scores."
    agreement = "\n".join(agreement_lines) if agreement_lines else "- Only one grader file was available."

    avg_no = condition_means["no_skill"]
    avg_skill = condition_means["skill_guided"]
    avg_gain = avg_skill - avg_no
    seed_path = outdir / "_private" / "randomization_seed.txt"
    seed_line = ""
    if seed_path.exists():
        seed_line = f"\n- Randomization seed, disclosed after grading: `{seed_path.read_text(encoding='utf-8').strip()}`"

    return f"""# Live Unblinded Skill-Use Evaluation Report

This report joins frozen blinded grades to `_private/private_mapping.csv`.

## Result Summary

- Average `no_skill`: **{fmt(avg_no)} / 100**
- Average `skill_guided`: **{fmt(avg_skill)} / 100**
- Average paired gain: **{fmt(avg_gain)} points**
{seed_line}

## Packet Scores

| packet | case | condition | graders | mean | min | max |
|---|---|---|---:|---:|---:|---:|
{packet_table}

## Paired Case Differences

| case | no_skill | skill_guided | difference |
|---|---:|---:|---:|
{case_table}

## Agreement Check

{agreement}

## Interpretation

This evaluation supports a narrow claim: in these four live generated tasks,
skill-guided outputs were more useful when usefulness is defined as producing
inspectable research artifacts, traceability markers, explicit authorship
boundaries, validation gates, and author-owned decision points.

{exception_note}

It does not show that skill-guided agents are always better, that the artifacts
are empirically correct, or that the final scholarly claims are valid. Those
remain author responsibilities and require source/data inspection.

## Limits

- Generation was not blind to condition.
- Graders were condition-blinded but may infer style from packet contents.
- Agent graders are not independent human experts.
- Cases are controlled scenarios, not full field deployments.
- The rubric favors traceable research workflow behavior by design; it should not
  be used to rank final prose quality.
"""


def report(outdir: Path) -> None:
    text = summarize_grades(outdir)
    write_text(outdir / "unblinded_report.md", text)


def validate_grade_rows(rows: list[dict[str, str]], mapping: dict[str, dict[str, str]]) -> None:
    seen: set[tuple[str, str]] = set()
    errors: list[str] = []
    valid_packets = set(mapping)
    expected_fields = set(GRADE_FIELDS)

    for index, row in enumerate(rows, start=2):
        missing_fields = expected_fields - set(row)
        if missing_fields:
            errors.append(f"row {index}: missing fields {sorted(missing_fields)}")
            continue

        packet_id = row["packet_id"]
        grader_id = row["grader_id"]
        if packet_id not in valid_packets:
            errors.append(f"row {index}: unknown packet_id {packet_id}")
            continue
        if row["case_id"] != mapping[packet_id]["case_id"]:
            errors.append(
                f"row {index}: case_id {row['case_id']} does not match packet {packet_id} case {mapping[packet_id]['case_id']}"
            )
        key = (grader_id, packet_id)
        if key in seen:
            errors.append(f"row {index}: duplicate grade for {grader_id}/{packet_id}")
        seen.add(key)

        component_sum = 0.0
        for field, limit in SCORE_FIELDS:
            try:
                value = float(row[field])
            except ValueError:
                errors.append(f"row {index}: {field} is not numeric")
                continue
            if value < 0 or value > limit:
                errors.append(f"row {index}: {field}={value} outside 0-{limit}")
            component_sum += value

        total_text = row["total_0_100"]
        if total_text:
            try:
                total = float(total_text)
            except ValueError:
                errors.append(f"row {index}: total_0_100 is not numeric")
                continue
            if total < 0 or total > 100:
                errors.append(f"row {index}: total_0_100={total} outside 0-100")
            if abs(total - component_sum) > 0.1:
                errors.append(f"row {index}: total {total} != component sum {component_sum}")

    if errors:
        raise SystemExit("Invalid grade CSV input:\n" + "\n".join(f"- {error}" for error in errors))


def choose_seed(seed: int | None) -> int:
    return seed if seed is not None else secrets.randbits(64)


def main() -> None:
    parser = argparse.ArgumentParser()
    subparsers = parser.add_subparsers(dest="command", required=True)

    package_parser = subparsers.add_parser("package")
    package_parser.add_argument("--outdir", type=Path, default=DEFAULT_OUTDIR)
    package_parser.add_argument("--seed", type=int, default=None)
    package_parser.add_argument("--clean", action="store_true")

    report_parser = subparsers.add_parser("report")
    report_parser.add_argument("--outdir", type=Path, default=DEFAULT_OUTDIR)

    args = parser.parse_args()
    if args.command == "package":
        package(args.outdir, choose_seed(args.seed), args.clean)
    elif args.command == "report":
        report(args.outdir)


if __name__ == "__main__":
    main()
